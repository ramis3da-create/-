const I18nContext = React.createContext({
  lang: 'ar',
  t: (path) => path,
  setLang: () => {},
});

export function I18nProvider({ children }) {
  const [lang, setLang] = React.useState(() => localStorage.getItem('lang') || 'ar');

  React.useEffect(() => {
    localStorage.setItem('lang', lang);
    if (lang === 'ar') {
      document.documentElement.lang = 'ar';
      document.documentElement.dir = 'rtl';
    } else if (lang === 'py' || lang === 'php') {
      document.documentElement.lang = 'en';
      document.documentElement.dir = 'ltr';
    } else {
      document.documentElement.lang = 'en';
      document.documentElement.dir = 'ltr';
    }
  }, [lang]);

  const t = React.useCallback((keyPath) => {
    const parts = keyPath.split('.');
    let node = dictionaries[lang];
    for (const p of parts) {
      node = node?.[p];
      if (node === undefined) return keyPath;
    }
    return node;
  }, [lang]);

  const value = React.useMemo(() => ({ lang, setLang, t }), [lang, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return React.useContext(I18nContext);
}


